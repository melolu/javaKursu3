package Gun15;

public class _03_ForLoop {
    public static void main(String[] args) {
        //0dan 10a kadar sayilari ekrana yazdirinz
        //10dan 0a kadar sayilari ekrana yazdiriniz

        for (int i=0;i<=10;i++){
            System.out.print(" i=" +i );
        }
        for (int j=10;j>=0;j--){
            System.out.print(" j=" + j );
        }
    }
}
