package Gun15;

public class _02_ForLoop {
    public static void main(String[] args) {
        //1den 5e kadar olan sayilari yazdirin
        int i=1;
        while (i<=5){
            System.out.println("i = "+i);
            i++;
        }

        //for dongusu

        for (i=1;i<=5;i++){
            System.out.println("i = " + i);
        }
    }
}
